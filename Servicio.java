public class Servicio {
    private int id_serv;
    private String tipoServ;
    private String fechaServ;
    private int codigoServ;
    private String nombreServ;
    private String descripServ;
    private String estadoServ;
    private int categoriaServ;
    private int montoServ;
    private int montoExtraServ;

    public int getId_serv() {
        return id_serv;
    }

    public void setId_serv(int id_serv) {
        this.id_serv = id_serv;
    }

    public String getTipoServ() {
        return tipoServ;
    }

    public void setTipoServ(String tipoServ) {
        this.tipoServ = tipoServ;
    }

    public String getFechaServ() {
        return fechaServ;
    }

    public void setFechaServ(String fechaServ) {
        this.fechaServ = fechaServ;
    }

    public int getCodigoServ() {
        return codigoServ;
    }

    public void setCodigoServ(int codigoServ) {
        this.codigoServ = codigoServ;
    }

    public String getNombreServ() {
        return nombreServ;
    }

    public void setNombreServ(String nombreServ) {
        this.nombreServ = nombreServ;
    }

    public String getDescripServ() {
        return descripServ;
    }

    public void setDescripServ(String descripServ) {
        this.descripServ = descripServ;
    }

    public String getEstadoServ() {
        return estadoServ;
    }

    public void setEstadoServ(String estadoServ) {
        this.estadoServ = estadoServ;
    }

    public int getCategoriaServ() {
        return categoriaServ;
    }

    public void setCategoriaServ(int categoriaServ) {
        this.categoriaServ = categoriaServ;
    }

    public int getMontoServ() {
        return montoServ;
    }

    public void setMontoServ(int montoServ) {
        this.montoServ = montoServ;
    }

    public int getMontoExtraServ() {
        return montoExtraServ;
    }

    public void setMontoExtraServ(int montoExtraServ) {
        this.montoExtraServ = montoExtraServ;
    }
    
    
    
    
}
